    public void actualizarTabla() {
        lics = ConsultasObjetos.consultaMuchos("licenciatura", null, null, ConectarBase.conectado());
        modelo = (DefaultTableModel) TablaPeriodos.getModel();
        ArrayList planes = new ArrayList();
        planes = ConsultasObjetos.consultaMuchos("plan_estudios", null, null, ConectarBase.conectado());
        if (planes.isEmpty())
        {
            Mensaje.error(this, "No hay planes de estudio registrados");
        } else
        {
            modelo.setRowCount(0);
            for (Object p : planes)
            {
                PlanEstudios plan = (PlanEstudios) p;
                modelo.addRow(new Object[]
                {
                    plan.getIdPlan(), plan.getPlanEstudios(), buscaLic(plan.getClaveCarrera(), null)
                });
                System.out.println(((PlanEstudios) p).getPlanEstudios());
            }
        }
    }
    
    private void edicion() {
        if (edicion)
        {
            edicion = false;
        } else
        {
            edicion = true;
        }
    }
    
    private void cancelar() {
        edicion();
        CtrlInterfaz.limpia(jTIdPlan, jTPlan);
        CtrlInterfaz.habilita(false, jTIdPlan, jTPlan, jBCancelar);
        CtrlInterfaz.habilita(true, jBAceptar, jBEliminar, jBModificar);
        jBAceptar.setText("Nuevo");
        jBModificar.setText("Modificar");
    }
    
    public String buscaLic(String id, String licenciatura) {
        if (licenciatura != null)
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if ((lic.getLicenciatura()).equals(licenciatura))
                {
                    return lic.getIdLicenciatura();
                }
            }
        } else
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if (lic.getIdLicenciatura().equals(id))
                {
                    return lic.getLicenciatura();
                }
            }
        }
        return null;
    }
    
    public void llenaComboLic() {
        jCLicenciatura.removeAllItems();
        for (int i = 0; i < lics.size(); i++)
        {
            jCLicenciatura.addItem(((Licenciatura) lics.get(i)).getLicenciatura());
        }
    }
