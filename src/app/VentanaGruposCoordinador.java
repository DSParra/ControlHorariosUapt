    private void actualizarTabla(int valor) {
        modelo = (DefaultTableModel) tablaGrupos.getModel();
        if (valor == 1)
        {
            if (grupos.isEmpty())
            {
                Mensaje.error(this, "No hay grupos registrados");
            } else
            {
                modelo.setRowCount(0);
                for (Object g : grupos)
                {
                    Grupo grupo = (Grupo) g;
                    if (grupo.getId_licenciatura().equals(vtn.lic))
                    {
                        modelo.addRow(new Object[]
                        {
                            grupo.getIdGrupo(), grupo.getNombreGrupo(), buscaLic(grupo.getId_licenciatura(), null)
                        });
                    }
                }
                btnBuscar.setText("Buscar");
            }
        } else if (valor == 2)
        {
            grupos = ConsultasObjetos.consultaMuchos("grupo", "nombre_grupo", txtIdBusqueda.getText(), null, null, "nombre_grupo", ConectarBase.conectado());
            if (grupos.isEmpty())
            {
                Mensaje.error(this, "No hay grupos registrados");
            } else
            {
                modelo.setRowCount(0);
                for (Object g : grupos)
                {
                    Grupo grupo = (Grupo) g;
                    if (grupo.getId_licenciatura().equals(vtn.lic))
                    {
                        modelo.addRow(new Object[]
                        {
                            grupo.getIdGrupo(), grupo.getNombreGrupo(), buscaLic(grupo.getId_licenciatura(), null)
                        });
                    }
                }
                btnBuscar.setText("Todo");
                txtIdBusqueda.setText("");

            }
        }
    }

    private void edicion() {
        if (edicion)
        {
            edicion = false;
        } else
        {
            edicion = true;
        }
    }

    public String buscaLic(String id, String licenciatura) {
        if (licenciatura != null)
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if ((lic.getLicenciatura()).equals(licenciatura))
                {
                    return lic.getIdLicenciatura();
                }
            }
        } else
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if (lic.getIdLicenciatura().equals(id))
                {
                    return lic.getLicenciatura();
                }
            }
        }
        return null;
    }

    private void cancelar() {
        edicion();
        CtrlInterfaz.limpia(txtId, txtNombre);
        CtrlInterfaz.habilita(false, txtId, txtNombre, btnCancelar);
        CtrlInterfaz.habilita(true, btnNuevo, btnEliminar, btnModificar, btnExportar);
        btnNuevo.setText("Nuevo");
        btnModificar.setText("Modificar");
    }

    private void importaBD() {
        lics = ConsultasObjetos.consultaMuchos("licenciatura", null, null, null, null, "nombre", ConectarBase.conectado());
        grupos = ConsultasObjetos.consultaMuchos("grupo", null, null, null, null, "nombre_grupo", ConectarBase.conectado());
        ConectarBase.desconectaBD();
    }
