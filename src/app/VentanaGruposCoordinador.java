    private void actualizarTabla(int valor) {
        lics = ConsultasObjetos.consultaMuchos("licenciatura", null, null, null, null, ConectarBase.conectado());
        modelo = (DefaultTableModel) TablaGrupos.getModel();
        ArrayList grupos = new ArrayList();
        if (valor == 1)
        {
            grupos = ConsultasObjetos.consultaMuchos("grupo", "id_licenciatura", vtn.lic, null, null, ConectarBase.conectado());
            if (grupos.isEmpty())
            {
                Mensaje.error(this, "No hay grupos registrados");
            } else
            {
                modelo.setRowCount(0);
                for (Object g : grupos)
                {
                    Grupo grupo = (Grupo) g;
                    modelo.addRow(new Object[]
                    {
                        grupo.getIdGrupo(), grupo.getNombreGrupo(), buscaLic(grupo.getId_licenciatura(), null)
                    });
                    System.out.println(((Grupo) g).getIdGrupo());
                }
            }
        } else if (valor == 2)
        {
            grupos = ConsultasObjetos.consultaMuchos("grupo", "id_grupo", jtIDBusqeuda.getText(), null, null, ConectarBase.conectado());
            if (grupos.isEmpty())
            {
                Mensaje.error(this, "No hay grupos registrados");
            } else
            {
                modelo.setRowCount(0);
                for (Object g : grupos)
                {
                    Grupo grupo = (Grupo) g;
                    modelo.addRow(new Object[]
                    {
                        grupo.getIdGrupo(), grupo.getNombreGrupo(), buscaLic(grupo.getId_licenciatura(), null)
                    });
                    System.out.println(((Grupo) g).getIdGrupo());
                }
            }
        }
    }

    private void edicion() {
        if (edicion)
        {
            edicion = false;
        } else
        {
            edicion = true;
        }
    }

    public String buscaLic(String id, String licenciatura) {
        if (licenciatura != null)
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if ((lic.getLicenciatura()).equals(licenciatura))
                {
                    return lic.getIdLicenciatura();
                }
            }
        } else
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if (lic.getIdLicenciatura().equals(id))
                {
                    return lic.getLicenciatura();
                }
            }
        }
        return null;
    }

