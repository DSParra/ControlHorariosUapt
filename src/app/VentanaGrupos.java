    private void actualizarTabla(int valor) {
        lics = ConsultasObjetos.consultaMuchos("licenciatura", null, null, null, null, "nombre", ConectarBase.conectado());
        modelo = (DefaultTableModel) TablaGrupos.getModel();
        ArrayList grupos = new ArrayList();
        switch (valor)
        {
            case 1:
                grupos = ConsultasObjetos.consultaMuchos("grupo", null, null, null, null, "nombre_grupo", ConectarBase.conectado());
                if (grupos.isEmpty())
                {
                    Mensaje.error(this, "No hay grupos registrados");
                } else
                {
                    modelo.setRowCount(0);
                    for (Object g : grupos)
                    {
                        Grupo grupo = (Grupo) g;
                        modelo.addRow(new Object[]
                        {
                            grupo.getIdGrupo(), grupo.getNombreGrupo(), buscarLic(grupo.getId_licenciatura(), null)
                        });
                        System.out.println(((Grupo) g).getIdGrupo());
                        btnBusca.setText("Buscar");
                    }
                }
                break;
            case 2:
                grupos = ConsultasObjetos.consultaMuchos("grupo", "nombre_grupo", jtIDBusqeuda.getText(), null, null, "nombre_grupo", ConectarBase.conectado());
                if (grupos.isEmpty())
                {
                    Mensaje.error(this, "No hay grupos registrados");
                } else
                {
                    modelo.setRowCount(0);
                    for (Object g : grupos)
                    {
                        Grupo grupo = (Grupo) g;
                        modelo.addRow(new Object[]
                        {
                            grupo.getIdGrupo(), grupo.getNombreGrupo(), buscarLic(grupo.getId_licenciatura(), null)
                        });
                        System.out.println(((Grupo) g).getIdGrupo());
                        jtIDBusqeuda.setText("");
                        btnBusca.setText("Todo");
                    }
                }
                break;
            case 3:
                if (jCLicenciaturaFiltro.getSelectedIndex() == 0)
                {
                    actualizarTabla(1);
                } else
                {
                    grupos = ConsultasObjetos.consultaMuchos("grupo", "id_licenciatura", buscarLic(null, jCLicenciaturaFiltro.getSelectedItem().toString()), null, null, "nombre_grupo", ConectarBase.conectado());
                    if (grupos.isEmpty())
                    {
                        Mensaje.error(this, "No hay grupos registrados");
                    } else
                    {
                        modelo.setRowCount(0);
                        for (Object g : grupos)
                        {
                            Grupo grupo = (Grupo) g;
                            modelo.addRow(new Object[]
                            {
                                grupo.getIdGrupo(), grupo.getNombreGrupo(), buscarLic(grupo.getId_licenciatura(), null)
                            });
                            System.out.println(((Grupo) g).getIdGrupo());
                            btnBusca.setText("Todo");
                        }
                    }
                }
                break;
            default:
                break;
        }
    }

    private void edicion() {
        if (edicion)
        {
            edicion = false;
        } else
        {
            edicion = true;
        }
    }

    public String buscarLic(String id, String licenciatura) {
        if (licenciatura != null)
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if ((lic.getLicenciatura()).equals(licenciatura))
                {
                    return lic.getIdLicenciatura();
                }
            }
        } else
        {
            for (Object l : lics)
            {
                Licenciatura lic = (Licenciatura) l;
                if (lic.getIdLicenciatura().equals(id))
                {
                    return lic.getLicenciatura();
                }
            }
        }
        return null;
    }

    private void cancelar() {
        edicion();
        CtrlInterfaz.limpia(jTIdGrupo, jTNombreGrupo);
        CtrlInterfaz.habilita(false, jTIdGrupo, jTNombreGrupo, jCLicenciatura, jBCancelar);
        CtrlInterfaz.habilita(true, jBAceptar, jBEliminar, jBModificar, btnExportar);
        jBAceptar.setText("Nuevo");
        jBModificar.setText("Modificar");
    }

    private void llenaComboLic() {
        jCLicenciatura.removeAllItems();
        jCLicenciaturaFiltro.removeAllItems();
        jCLicenciaturaFiltro.addItem("TODAS");
        for (int i = 0; i < lics.size(); i++)
        {
            jCLicenciatura.addItem(((Licenciatura) lics.get(i)).getLicenciatura());
            jCLicenciaturaFiltro.addItem(((Licenciatura) lics.get(i)).getLicenciatura());
        }
    }

    private int buscarCombo(String texto) {
        for (int i = 0; i < jCLicenciatura.getItemCount(); i++)
        {
            if (texto.equals(jCLicenciatura.getItemAt(i)))
            {
                return i;
            }
        }
        return 0;
    }
